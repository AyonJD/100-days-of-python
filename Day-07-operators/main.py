# Floor Division Operator ------> // This is a division but removes the decimal part of the result. Returns only the integer part of the result.

print(8 // 3)  # 2

# Modulo Operator ------> % This is a division but returns the remainder of the division. 

print(5 % 3)  # 2

# Exponent Operator ------> ** This is a power operator. It returns the first number to the power of the second number.

print(2 ** 3)  # 8
